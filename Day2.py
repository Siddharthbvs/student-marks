# List Example
my_list = [10, 20, 30, 40, 50]
print("List Example:")
print("Element at index 0:", my_list[0])  # First element
print("Element at index 2:", my_list[2])  # Third element
print("Element at index -1:", my_list[-1])  # Last element

# Tuple Example
my_tuple = (100, 200, 300, 400, 500)
print("\nTuple Example:")
print("Element at index 1:", my_tuple[1])  # Second element
print("Element at index 3:", my_tuple[3])  # Fourth element
print("Element at index -2:", my_tuple[-2])  # Second last element

# Dictionary Example
my_dict = {
    'a': 10,
    'b': 20,
    'c': 30,
    'd': 40,
    'e': 50
}
print("\nDictionary Example:")
print("Value associated with key 'a':", my_dict['a'])  # Access by key
print("Value associated with key 'c':", my_dict['c'])  # Access by key


